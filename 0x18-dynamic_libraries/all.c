#include <stdlib.h>
#include <stdio.h>

/**
 * add - add two nos
 * @a: first int
 * @b: second int
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two nos
 * @a: first int
 * @b: second int
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two nos
 * @a: first int
 * @b: second int
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * divi - divides two nos
 * @a: first int
 * @b: second int
 * Return: quotient
 */
int divi(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of two nos
 * @a: first int
 * @b: second int
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
